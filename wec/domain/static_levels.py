# wec/domain/static_levels.py
"""Статические (регламентные) уровни и параметры гидроузла.

* **nrl** – *Нормальный подпорный уровень* (НПУ), м. Верхняя рабочая
  отметка, до которой обычно удерживается зеркало водохранилища.
* **dead** – *Уровень мёртвого объёма* (УМО), м. Ниже этой отметки
  эксплуатационная сработка запрещена (дно водозабора, минимальный
  экологический уровень и т.п.).
* **installed_capacity** – *Установленная мощность* станции, МВт. Верхняя
  граница генерации, которую нельзя превышать даже при большом напоре
  и расходе.
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass(slots=True)
class StaticLevels:
    """Контейнер фиксированных уровней и мощности ГЭС."""

    # ------------------------------------------------------------------
    # Отметки (уровни), м БС
    # ------------------------------------------------------------------
    nrl: float  # НПУ – Normal Retaining Level, верхняя эксплуатационная граница
    dead: float  # УМО – Dead Volume Level, нижняя эксплуатационная граница

    # ------------------------------------------------------------------
    # Энергетический параметр
    # ------------------------------------------------------------------
    installed_capacity: float  # МВт – суммарная установленная мощность ГЭС
